/**********************************************************************
 * @brief		ARM System Timer Interrupt Handler.
 * @param[in]	None
 * @return	    None
 **********************************************************************/
void SysTick_Handler (void)
{
    if (s_un32SysTimerVal)
    {
        s_un32SysTimerVal--;
    }
#if SUPPORT_MENU_UI
    EX_SoftTimer_TickIsr();
#endif
}

/**********************************************************************
 * @brief		Waiting by time(ms)
 * @param[in]	un32TimeMS : Milisecond time to wait.
 * @return	    None
 **********************************************************************/
static void SYSTICK_Wait (uint32_t un32TimeMS)
{
    s_un32SysTimerVal = un32TimeMS;
    while(s_un32SysTimerVal);
}

/**********************************************************************
 * @brief		Start blinking LED on Starter Kit.
 * @param[in]	None
 * @return	    None
 **********************************************************************/
static void TC_LED_StartBlinking(void)
{
    uint32_t un32LedID;

    /* Turn on */
    for (un32LedID = LED0_PIN_ID; un32LedID <= LED3_PIN_ID; un32LedID++)
    {
        HAL_PCU_SetOutputBit((PCU_ID_e)LED_PORT, (PCU_PIN_ID_e)un32LedID, PCU_OUTPUT_BIT_SET);
        SYSTICK_Wait(100);
    }

    /* Turn off */
    for (un32LedID = LED0_PIN_ID; un32LedID <= LED3_PIN_ID; un32LedID++)
    {
        HAL_PCU_SetOutputBit((PCU_ID_e)LED_PORT, (PCU_PIN_ID_e)un32LedID, PCU_OUTPUT_BIT_CLEAR);
        SYSTICK_Wait(100);
    }
}

